package com.mimaraslan.v1;

public abstract class Duck {
	
	public void quack() {
		System.out.println("Duck quack");
	}
	
	public void swim() {
		System.out.println("Duck swim");
	}
	
	public void display() {
		System.out.println("Duck display");
	}
	
	public void fly() {
		System.out.println("Duck fly");
	}
}